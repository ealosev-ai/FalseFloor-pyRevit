import os


def _detect_lang():
    env_lang = (os.environ.get("FALSEFLOOR_LANG") or "").strip().lower()
    if env_lang in ("ru", "en"):
        return env_lang

    try:
        import System  # type: ignore

        code = (
            System.Globalization.CultureInfo.CurrentUICulture.TwoLetterISOLanguageName
        )
        code = (code or "").lower()
        if code in ("ru", "en"):
            return code
    except Exception:
        pass

    return "ru"


