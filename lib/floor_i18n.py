# -*- coding: utf-8 -*-

import os


def _detect_lang():
    # 1. Process-level env var (set by os.environ or inherited at startup)
    env_lang = (os.environ.get("RAISEDFLOOR_LANG") or "").strip().lower()
    if env_lang in ("ru", "en"):
        return env_lang

    # 2. User-level env var persisted in Windows registry via .NET
    try:
        import System  # type: ignore

        val = System.Environment.GetEnvironmentVariable(
            "RAISEDFLOOR_LANG",
            System.EnvironmentVariableTarget.User,
        )
        val = (val or "").strip().lower()
        if val in ("ru", "en"):
            return val
    except Exception:
        pass

    # 3. Auto-detect from system UI culture
    try:
        import System  # type: ignore

        code = (
            System.Globalization.CultureInfo.CurrentUICulture.TwoLetterISOLanguageName
        )
        code = (code or "").lower()
        if code in ("ru", "en"):
            return code
    except Exception:
        pass

    return "ru"


